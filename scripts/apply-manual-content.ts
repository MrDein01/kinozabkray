function htmlFromText(text: string) {
  return text
    .split(/\n+/)
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => `<p>${part}</p>`)
    .join('');
}

async function setHome(key: string, value: string) {
  await prisma.homePage.upsert({
    where: { key },
    update: { value },
    create: { key, value },
  });
}

async function upsertHome() {
  for (const item of homeSettings) {
    await setHome(item.key, item.value);
  }
}

async function upsertAdmin() {
  const hashedPassword = await bcrypt.hash(adminPassword, 10);

  await prisma.user.deleteMany({
    where: {
      email: { in: ['admin@kino75.ru', adminEmail] },
    },
  });

  await prisma.user.create({
    data: {
      email: adminEmail,
      password: hashedPassword,
      name: 'Администратор',
      role: 'ADMIN',
    },
  });
}

async function replaceNews() {
  await prisma.news.deleteMany({});

  for (const item of newsData) {
    await prisma.news.create({
      data: {
        title: item.title,
        slug: item.slug,
        excerpt: item.excerpt,
        content: htmlFromText(item.content),
        imageUrl: item.imageUrl,
        published: true,
        publishedAt: new Date(item.date),
      },
    });
  }
}

async function replaceCinemas() {
  await prisma.cinema.deleteMany({});
  for (const item of cinemasData) {
    await prisma.cinema.create({ data: { ...item, active: true } });
  }
}

async function replaceAntiCorruption() {
  await prisma.antiCorruption.deleteMany({});
  for (const item of antiCorruptionData) {
    await prisma.antiCorruption.create({
      data: { ...item, publishedAt: new Date('2026-05-08') },
    });
  }
}

async function replaceProsecutor() {
  await prisma.prosecutorExplanation.deleteMany({});
  for (const item of prosecutorData) {
    await prisma.prosecutorExplanation.create({
      data: {
        title: item.title,
        slug: item.slug,
        excerpt: item.excerpt,
        content: htmlFromText(item.content),
        publishedAt: new Date(item.date),
      },
    });
  }
}

async function syncCounters() {
  const [newsCount, cinemasCount, servicesCount, documentsCount] = await Promise.all([
    prisma.news.count(),
    prisma.cinema.count({ where: { active: true } }),
    prisma.service.count({ where: { active: true } }),
    prisma.antiCorruption.count(),
  ]);

  await setHome('stats_news_value', String(newsCount));
  await setHome('stats_news_label', 'новостей');
  await setHome('stats_cinemas_value', String(cinemasCount));
  await setHome('stats_cinemas_label', 'кинотеатров');
  await setHome('stats_services_value', String(servicesCount));
  await setHome('stats_services_label', 'услуг');
  await setHome('stats_documents_value', String(documentsCount));
  await setHome('stats_documents_label', 'документов');
}

async function main() {
  console.log('Обновляю главную и подвал...');
  await upsertHome();

  console.log('Меняю администратора...');
  await upsertAdmin();

  console.log('Заполняю новости с изображениями и полным списком для админки...');
  await replaceNews();

  console.log('Заполняю 9 кинотеатров...');
  await replaceCinemas();

  console.log('Заполняю раздел противодействия коррупции...');
  await replaceAntiCorruption();

  console.log('Заполняю все материалы раздела прокурор разъясняет...');
  await replaceProsecutor();

  console.log('Обновляю счетчики админки и главной...');
  await syncCounters();

  console.log('Готово. Данные обновлены.');
}

